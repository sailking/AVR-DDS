	// set PB0-PB3 as output
	DDRB = 0x0F;
	// set PB0-PB3 on high-level
	PORTB |= 0x0F;		// Required for DMM Board DMM Board 2013
	int i;
	for (i =0;i<10;i++)
	{
		_delay_ms(10);
	}
